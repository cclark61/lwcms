//****************************************************************
// jQuery Intializer
//****************************************************************
$(document).ready(function()
{
	//****************************************************
	// Site Delete Verification
	//****************************************************
	$('.site_delete').click(function () {
		return confirm("Are you sure you want to permanently DELETE this site and all of it's associated data?");
	});

	//**************************************************************************
	//**************************************************************************
	// Code Editor / Static Content
	//**************************************************************************
	//**************************************************************************
	$('#rename_file').click(function () {
		if ($(this).prop('checked')) {
			$('input[name="new_file_name"]').show()
		}
		else {
			$('input[name="new_file_name"]').hide()
		}
	});

	//**************************************************************************
	//**************************************************************************
	// Revision Modal
	//**************************************************************************
	//**************************************************************************
	$('.view_version').click('show', function () {
	    $("#revision_modal .modal-body iframe").attr('src', $(this).attr('content_link'));
	});

});

